    @Override
    public void setProjectID(int p) 
            throws Exception
    {
        project = (Project)MetaFactory.getInstance().getFactory("Project").get(p);
        hazards = MetaFactory.getInstance().getFactory("Hazard").getEntries(p);
        ArrayList<String> htypes = new ArrayList<>();
        for (Hazard h : hazards) {
            String t = h.getAttributeValue("GroupingType");
            if (!htypes.contains(t))
                htypes.add(t);
        }
        htypes.sort(Collator.getInstance());
        DefaultListModel<String> dlm = new DefaultListModel<>();
        for (String s : htypes)
            dlm.addElement(s);
        typesList.setModel(dlm);
    }
    
